#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;
	char	*tmp;

	i = 0;
	tmp = dest;
	while (i < n)
	{
		*tmp = *(char *)src;
		tmp++;
		i++;
	}
}

