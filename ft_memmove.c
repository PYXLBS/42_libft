#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	m;

	m = 0;
	if (dest > src)
	{
		while (n > 0)
		{
			n--;
			((unsigned char *)dest)[n] = ((unsigned char *)src)[n];
		}
	}
	else
	{
		while (n > m)
		{
			((unsigned char *)dest)[m] = ((unsigned char *)src)[m];
			m++;
		}
	}
	return (dest);
}
