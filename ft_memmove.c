#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	char	*tmp;
	size_t	i;

	if (dest <= src)
	{
		tmp = dest;
		while (n--)
			*tmp++ = *src++;
	}
}

