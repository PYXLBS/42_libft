#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	d;
	size_t	s;

	d = 0;
	s = 0;
	while (src[s] != '\0')
	{
		if (size > d + 1)
		{
			dst[d] = src[s];
			d++;
		}
		s++;
	}
	if (size != 0)
		dst[d] = '\0';
	return (s);
}
