#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t i;

	i = 0;
	while (src[i] != '\0')
	{
		if (size > i + 1)
			dst[i] = src[i];
		i++;
	}
	if (size != 0)
		dst[i] = '\0';
	return (i);
}

