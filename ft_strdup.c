#include "libft.h"

char	*ft_strdup(const char *s)
{
	int	t;
	char	*u;

	t = 0;
	u = malloc(t + 1);
	if (u == NULL)
		return (0);
	while (s[t] != '\0')
	{
		u[t] = s[t];
		t++;
	}
	if (u[t] == '\0')
		return (u);
	return (0);
}
